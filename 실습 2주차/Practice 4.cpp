// 최소공배수 구하기
#include <iostream>
using namespace std;

int main()
{
	int a, b, c;
	int LCM, MUL;
	
	cout << "주의사항 : a 보다 b 가 더 커야합니다!" << endl;
	
	cin >> a;
	cin >> b;

	MUL = a * b;

	while (b != 0) {

		c = a % b;
		a = b;
		b = c;
	}

	LCM = MUL / a;
	
	cout << LCM;

	return 0;
}
